<template >
  <div class="container-card">
    <div class="">
      <div class="ms-2 me-2 movie_card item">
        <img
          :src="'http://image.tmdb.org/t/p/w500/' + movie.poster_path"
          class="card-img-top"
          alt="..."
        />
        <div class="film_info">
          <div class="list-icon-left">
            <a
              ><i class="fas fa-play" style="background: white; color: black">
              </i
            ></a>

            <a
              ><i class="fas fa-plus toolTip">
                <span class="toolTiptext-sm tool-span-sm"> Listeme ekle </span>
              </i></a
            >
            <a @click="showDetail(movie)">
              <i class="fas fa-chevron-down toolTip">
                <span class="toolTiptext-sm tool-span-sm">
                  Daha fazla bilgi
                </span>
              </i>
            </a>
          </div>
        </div>
      </div>
      <modals
        :modalData="modalData"
        :modalId="modalId"
        :videoId="videoId"
        v-model="isOpen"
      />
    </div>
  </div>
</template>
<script>
import axios from "axios";
import modals from "@/components/modal/modals";

export default {
  components: { modals },

  props: {
    movie: {
      type: Object,
      required: true,
      isOpen: false,
    },
  },
  data() {
    return {
      isOpen: false,
      modalData: [],
      modalData: [],
      modalId: null,
      videoId: [],
      showLoading: true,
    };
  },
  methods: {
    async showDetail(movie) {
      this.showLoading = true;
      try {
        await axios
          .get(
            `https://api.themoviedb.org/3/movie/${movie.id}?api_key=7b97ca5600ae944d697e04e778928d05&language=en-US&append_to_response=videos,credits,release_dates,similar`
          )
          .then((response) => {
            console.log("iddetailvideos", response);
            this.modalId = response.data;
            this.modalData = movie;
          });

        console.log("movie", movie);
        this.isOpen = true;

        this.videoId = this.modalId.videos.results;
        console.log("this.videoId :>> ", this.videoId);
      } catch (error) {
        console.error(error);
      } finally {
        this.showLoading = false;
      }
    },
  },
};
</script>